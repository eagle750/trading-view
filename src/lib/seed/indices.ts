import type { IndexRow } from "@/lib/schemas";

function row(
  id: string,
  name: string,
  description: string,
  source: "NSE" | "BSE",
  base: number,
  seed: number,
): IndexRow {
  const spark = Array.from({ length: 20 }, (_, i) =>
    Math.max(0, base + Math.sin((seed + i) * 0.4) * (base * 0.02)),
  );
  return {
    id,
    name,
    description,
    source,
    ltp: base + (seed % 17) - 8,
    pctChg: ((seed % 100) - 50) / 100,
    ytdPct: ((seed * 3) % 200) / 10 - 10,
    sparkline: spark,
  };
}

